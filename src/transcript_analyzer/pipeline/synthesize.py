"""Push-based synthesis: digests, commitments, dossiers, study rollups, prep.

The thesis (from the CEO plan): pull-based personal memory goes unused —
note_categories sat at 0 rows after three attempts at organizing. So instead
of waiting to be asked, synthesis writes into the vault namespaces the user
already lives in:

  Digests/YYYY-MM-DD.md      what happened, with citations
  Digests/Commitments.md     every open "- [ ]" across all notes (mechanical)
  People/<Name>.md           dossiers for people seen >= 3 times
  Studies/<Name>.md          cross-source research rollups
  Prep/YYYY-MM-DD <mtg>.md   tomorrow's meetings: what you owe whom

Rigor rules (non-negotiable, enforced in code — not prompts):
  * Cadence guard: runs at most once per calendar day (meta key), never per
    20-minute sync cycle.
  * Citation gate: every LLM claim must carry a VERBATIM span that string-
    matches the cited source's text; claims that fail are dropped and counted.
  * Managed regions: only the block between synth markers is rewritten, so
    hand annotations survive (writer.write_managed).
  * Namespace isolation: writes are refused outside Digests/People/Studies/Prep.
  * Idempotent: re-running a day rewrites the same files, never duplicates.
  * Change detection: dossiers/studies only spend API calls when their input
    conversations changed (content-hash in meta).
"""
